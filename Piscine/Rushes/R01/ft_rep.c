#include <stdio.h>

int	ft_rep(int *m, int i)
{
	int j;

	j = i % 4;
	while (j)
	{
		if (m[i] == m[i - j])
			return (0);
		j--;
	}
	j = i / 4;
	while (j)
	{
		if (m[i] == m[i - 4 * j])
			return (0);
		j--;
	}
	return (1);
}
