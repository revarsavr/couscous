#include <unistd.h>

void	ft_putnbr(int n)
{
	char			c;
	unsigned int	m;

	if (n < 0)
	{
		c = '-';
		write(1, &c, 1);
		m = -n;
	}
	else
		m = n;
	if (m >= 10)
		ft_putnbr(m / 10);
	c = m % 10 + '0';
	write(1, &c, 1);
}
