int	ft_atoi(char *str)
{
	int i;
	int p;
	int r;

	i = 0;
	p = 0;
	r = 0;
	while (str[i] == ' ' || str[i] == '\t' || str[i] == '\n' \
	|| str[i] == '\v' || str[i] == '\f' || str[i] == '\r')
		i++;
	while (str[i] == '+' || str[i] == '-')
		if (str[i++] == '-')
			p++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		r *= 10;
		r -= str[i++] - '0';
	}
	if (!(p % 2))
		r = -r;
	return (r);
}
