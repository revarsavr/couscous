#include <unistd.h>

int	main(int argc, char **argv)
{
	_Bool c[128] = {0};

	while ((argc == 3) && *(argv[2]))
		c[(int)*(argv[2])++] = 1;
	while ((argc == 3) && *(argv[1]))
		write(1, &(*(argv[1])), c[(int)*(argv[1])] + (c[(int)*(argv[1])++] = 0));
	write(1, "\n", 1);
}
